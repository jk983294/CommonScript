# Test Coverage Program
# gcov should be run with the current directory the same as that when you invoked the compiler.
gcov [options] files
-a                            # Write individual execution counts for every basic block.
-b                            # Write branch frequencies to the output file, and write branch summary info to the standard output.
-c                             # Write branch frequencies as the number of branches taken, rather than the percentage of branches taken.
-f                            # Output summaries for each function in addition to the file level summary
-n                            # Do not create output file.
-l                            # Create long file names for included source files
