import numpy as np

a = np.array([[0, 0, 0], [10, 10, 10], [20, 20, 20], [30, 30, 30]])
b = np.array([[0, 1, 2], [0, 1, 2], [0, 1, 2], [0, 1, 2]])
print(a + b)

# 将b的值变成一维的 [0,1,2] 之后的加法, Numpy 检测到b的维度与a的维度匹配，所以将b扩展为之前的形式，得到相同的形状
b = np.array([0, 1, 2])
print(a + b)

# 将 a 变成一个列向量, Numpy还是根据两者的维度，自动将它们进行扩展然后进行计算
a = np.array([0, 10, 20, 30])
a.shape = 4, 1
print(a + b)