#!/bin/bash

map                     type
<c-x><c-l>              整行
<c-x><c-n>              当前缓冲区中的关键字
<c-x><c-i>              当前文件以及包含的文件中的关键字
<c-x><c-]>              tag completion
<c-x><c-f>              文件名
<c-x><c-d>              定义或宏定义
<c-x><c-v>              Vim 命令
<c-x><c-u>              用户自定义补全（通过 'completefunc' 定义）
<c-x><c-o>              Omni Completion（通过 'omnifunc' 定义）
<c-x>s                  拼写建议
<c-n>                   来调用下一个补全建议
<c-p>                   来调用上一个补全建议
